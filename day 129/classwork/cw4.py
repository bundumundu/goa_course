# .join() მეთოდი გამოიყენება iterable-ის (მაგ. სიის) ელემენტების ერთ სტრიქონად შერწყმისთვის.
# ამ მეთოდის წინ ვწერთ სტრიქონს, რომელიც გამოიყენება გამყოფად ელემენტებს შორის.

# მაგალითი 1:
words = ["მარიამი", "წავიდა", "ბიბლიოთეკაში"]
sentence = " ".join(words)
print(sentence)  # მარიამი წავიდა ბიბლიოთეკაში

# მაგალითი 2:
letters = ["A", "B", "C"]
joined = "-".join(letters)
print(joined)  # A-B-C
