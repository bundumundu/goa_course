# .split() მეთოდი გამოიყენება სტრიქონის დასაშლელად სიის ელემენტებად. 
# სტანდარტულად ის იყოფა space-ს (სიცარიელის) მიხედვით, თუმცა შეგვიძლია თავადაც მივუთითოთ გამყოფი სიმბოლო.

# მაგალითი 1:
text = "გამარჯობა როგორ ხარ"
words = text.split()
print(words)  # ['გამარჯობა', 'როგორ', 'ხარ']

# მაგალითი 2:
data = "apple,banana,grape"
fruits = data.split(",")
print(fruits)  # ['apple', 'banana', 'grape']
